public class LadderCondition1 {
    public static void main(String[] args) {
        int score = 69;
        if (score >= 90){
            System.out.println("Grade A");
        }
        else if (score >= 80){
            System.out.println("Grade B");
        }
        else if (score >= 70){
            System.out.println("Grade C");
        }
        else {
            System.out.println("Fail");
        }

    }
}
